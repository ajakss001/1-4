def ssn_vnts(month):
    if not isinstance(month, int) or month < 1 or month > 12:
        return "Требуется ввести реальный номер месяца"

    month_names = [
        "Январь", "Февраль", "Март", "Апрель", "Май",
        "Июнь", "Июль", "Август", "Сентябрь", "Октябрь",
        "Ноябрь", "Декабрь"
    ]

    seasons = {
        1: "Зима", 2: "Зима", 12: "Зима",
        3: "Весна", 4: "Весна", 5: "Весна",
        6: "Лето", 7: "Лето", 8: "Лето",
        9: "Осень", 10: "Осень", 11: "Осень"
    }

    descriptions = {
        "Зима": "За окном падал белый снег.",
        "Весна": "Птицы пели прекрасные песни.",
        "Лето": "Солнце светило ярче чем когда-либо.",
        "Осень": "Урожай был невероятным."
    }

    season = seasons[month]
    return f"Вы родились в {month_names[month - 1]}. {descriptions[season]}"